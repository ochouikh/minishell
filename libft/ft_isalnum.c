#include "libft.h"

int	ft_isalnum(int c)
{
	int	nb;

	if (!(c >= 48 && c <= 57) && !(c >= 65 && c <= 90)
		&& !(c >= 97 && c <= 122))
	{
		nb = 0;
	}
	else
	{
		nb = 1;
	}
	return (nb);
}
